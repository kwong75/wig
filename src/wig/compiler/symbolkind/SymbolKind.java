package wig.compiler.symbolkind;

public class SymbolKind {
	private int lineno;
	private int posno;

	public int getLineno() {
		return lineno;
	}

	public void setLineno(int lineno) {
		this.lineno = lineno;
	}

	public int getPosno() {
		return posno;
	}

	public void setPosno(int posno) {
		this.posno = posno;
	}

}
